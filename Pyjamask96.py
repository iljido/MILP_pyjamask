def Sbox( model, X, Y ):

    Ineq = (
    (2, 2, 4, 1, -5, -1, -2),\
    (2, -1, -2, -1, 0, 1, 1),\
    (0, -1, 0, -1, 2, 1, 1),\
    (1, 1, 1, 2, 0, -2, -1),\
    (2, -2, -1, -1, 1, 1, 0),\
    (0, 0, -1, -1, 1, 1, 2),\
    (2, -1, -1, 0, -1, 0, 1),\
    (1, 0, 1, -1, 0, 1, -1) )

    for ineq in Ineq:
        model.addConstr( ineq[0] +
                         ineq[1] * X[0] +
                         ineq[2] * X[1] +
                         ineq[3] * X[2] +
                         ineq[4] * Y[0] +
                         ineq[5] * Y[1] +
                         ineq[6] * Y[2]  >= 0 )


    return model

def Pyjamask( rounds, index ):

    model = Model()

    model.setParam( 'OutputFlag', 0)
    #roundKey
    K = [ [ model.addVar( vtype = GRB.BINARY ) for i in range(96) ] for j in range(rounds-1)  ]
    # input of each round
    X = [ [ model.addVar( vtype = GRB.BINARY ) for i in range(96) ] for j in range(rounds)  ]
    # after Sbox
    Y = [ [ model.addVar( vtype = GRB.BINARY ) for i in range(96) ] for j in range(rounds)  ]
    # after Linlayer
    Z = [ [ model.addVar( vtype = GRB.BINARY ) for i in range(96) ] for j in range(rounds-1)  ]
    
    for i in range(96):
        if i == index:
            model.addConstr( X[0][i] == 0 )
        else:
            model.addConstr( X[0][i] == 1 )
    

    for r in range(rounds - 1):
        # Sbox
        for i in range(32):
            model = Sbox( model, X[r][i:96:32], Y[r][i:96:32] )

        # row 0
        mat0 = Matrix( M0 )
        model = mat0.genConstrs( model, Y[r][0:32], Z[r][0:32] )

        mat1 = Matrix( M1 )
        model = mat1.genConstrs( model, Y[r][32:64], Z[r][32:64] )
#
        mat2 = Matrix( M2 )
        model = mat2.genConstrs( model, Y[r][64:96], Z[r][64:96] )
        
        for i in range(0,96):
            model.addConstr(Z[r][i] + K[r][i] == X[r+1][i])

    r = rounds - 1
    for i in range(32):
        model = Sbox( model, X[r][i:96:32], Y[r][i:96:32] )

    n = 0
    for i in range(96):
        n += Y[rounds - 1][i]
    #model.addConstr( n == 1 )
    model.setObjective( n, GRB.MINIMIZE )
    
    sumkey = 0
    for r in range(rounds-1):
        for i in range(96):
            sumkey += K[r][i]
    model.addConstr( sumkey >= 1 )

    S = set ( [i for i in range(96)] )

    for i in range(96):
        model.optimize()
        if model.status == GRB.OPTIMAL:
            #print ( model.getObjective().getValue() )
            #for i in range(96):
            #    print ( i, X[0][i].X )
            if round(model.ObjVal) == 1:
                obj = model.getObjective()

                for j in range(96):
                    var = obj.getVar( j )
                    val = var.getAttr( 'x' )
                    if abs ( round( val ) ) == 1:
                        S.remove( j )
                        print(j)
                        model.addConstr( var == 0 )
                        model.update()
                        break
            else:
                return S
        else:
            return S

    return S

